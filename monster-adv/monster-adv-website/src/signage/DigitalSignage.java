package signage;

public class DigitalSignage {
	private String id;
	private String name;
	private double coordinate_x;
	private double coordinate_y;
	
	public DigitalSignage(String id, String name, double coordinate_x,
			double coordinate_y) {
		this.id = id;
		this.name = name;
		this.coordinate_x = coordinate_x;
		this.coordinate_y = coordinate_y;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getCoordinate_x() {
		return coordinate_x;
	}
	public void setCoordinate_x(double coordinate_x) {
		this.coordinate_x = coordinate_x;
	}
	public double getCoordinate_y() {
		return coordinate_y;
	}
	public void setCoordinate_y(double coordinate_y) {
		this.coordinate_y = coordinate_y;
	}
	@Override
	public String toString() {
		return "DigitalSignage [id=" + id + ", name=" + name
				+ ", coordinate_x=" + coordinate_x + ", coordinate_y="
				+ coordinate_y + "]";
	}
	
	
}
